/**
 * HTTP
 * - Método
 * - URL
 * 
 * GET, POST, PUT, PATCH, DELETE
 * 
 * GET => Buscar um recurso do back-end
 * POST => Criar um recurso no back-end
 * PUT => Atualizar um recurso no back-end
 * PATCH => Atualizar uma informação especifica de um recurso no back-end
 * DELETE => Deleta um recurso no back-end
 *
 * GET/users => Buscando usuários no back-end
 * POST/users => Criar um usuário no back-end
 * 
 * StateFul - Stateless
 *  - Stateful => Salva apenas em mémoria
 *  - Stateless => Salva em bancos ou arquivos
 * 
 * cabeçalhos (Requisições/respostas) => Metadados 
 * 
 * HTTP state Code => ex. status 200, 201, 404 
 * 
 * Strems => Conseguimos trabalhos com dados sem presisar que eles tenham sido 
 * carregados completamente ex.(NetFlix e spotify)
 * 
 * Tipos de streams
 * - Readable - Streams de leitura (Ler os arquivos aos poucos)
 * - Weritable - Streams de escritas (Escrever os aquivos os poucos)
 * - Transform - Streams de transformação (Transforma um dado em outro)
 * 
 * Stdin => È uma string de entrada
 * Stdout => è uma string de saída*/


